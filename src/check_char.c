#include "fillit.h"

void	check_char(char *str, int fd)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == '.' || str[i] == '#' || str[i] == '\n')
			i++;
		else
			CLOSE_EXIT("error", fd);
	}
}
